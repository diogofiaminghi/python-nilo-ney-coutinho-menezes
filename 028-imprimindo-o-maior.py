a = int(input("Digite o primeiro número: "))
b = int(input("Digite o segundo número: "))

if a > b:
    print(f"O maior número é o primeiro: {a}")
if b > a:
    print(f"O maior número é o segundo: {b}")
if a == b:
    print("Os números são iguais.")

